    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        DefaultTableModel tblModel = (DefaultTableModel) tbl.getModel();
        int selectedRow = tbl.getSelectedRow();
        userid = Integer.parseInt(tblModel.getValueAt(selectedRow, 0).toString());
        txtUsername.setText(tblModel.getValueAt(selectedRow, 1).toString());
        txtPassword.setText(tblModel.getValueAt(selectedRow, 2).toString());
        cmbUserLevel.setSelectedItem(tblModel.getValueAt(selectedRow, 3));
        btnAdd.setEnabled(false);
        btnUpdate.setEnabled(true);
        btnDelete.setEnabled(true);
        btnClose.setText("Cancel");
    }//GEN-LAST:event_tblMouseClicked

