    private void populateTable() {
        int colCount;

        try {

            Connection cn = DB_connect.getConnection();
            Statement st = cn.createStatement();
            String query = "Select * FROM users";
            ResultSet res = st.executeQuery(query);
            ResultSetMetaData rsData = res.getMetaData();
            colCount = rsData.getColumnCount();

            DefaultTableModel tblModel = (DefaultTableModel) tbl.getModel();
            tblModel.setRowCount(0);
            while (res.next()) {

                Vector ColData = new Vector();
                for (int i = 0; i < colCount; i++) {
                    ColData.add(res.getInt("user_id"));
                    ColData.add(res.getString("username"));
                    ColData.add(res.getString("password"));
                    ColData.add(res.getString("user_role"));
                }
                tblModel.addRow(ColData);
            }

        } catch (SQLException ex) {

        }

    }

