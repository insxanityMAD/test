    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String username = txtUsername.getText().trim();
        String password = new String(txtPassword.getPassword());
        String confirmPassword = new String(txtConfirmPassword.getPassword());
        String userLevel = cmbUserLevel.getSelectedItem().toString();

        // Validation
        if (username.isEmpty() || password.isEmpty() || confirmPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields!");
            return;
        }

        if (!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match!");
            return;
        }

        try {
            Connection con = DB_connect.getConnection();
            PreparedStatement pst;

            // 👉 ADD USER
            if (check.equals("Add")) {

                String sql = "INSERT INTO users (username, password, user_role) VALUES (?, ?, ?)";
                pst = con.prepareStatement(sql);

                pst.setString(1, username);
                pst.setString(2, password);
                pst.setString(3, userLevel);

            } // 👉 UPDATE USER
            else if (check.equals("Update")) {

                String sql = "UPDATE users SET username = ?, password = ?, user_role = ? WHERE user_id = ?";
                pst = con.prepareStatement(sql);

                pst.setString(1, username);
                pst.setString(2, password);
                pst.setString(3, userLevel);
                pst.setInt(4, userid); // IMPORTANT: use selected user_id

            } else {
                JOptionPane.showMessageDialog(this, "No operation selected!");
                return;
            }

            int result = pst.executeUpdate();

            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Operation successful!");

                // Reset form
                txtUsername.setText("");
                txtPassword.setText("");
                txtConfirmPassword.setText("");
                cmbUserLevel.setSelectedIndex(0);

                setDefault();     // reset buttons + fields
                populateTable();  // refresh table
            } else {
                JOptionPane.showMessageDialog(this, "Operation failed.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

